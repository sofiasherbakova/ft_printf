#include "ft_printf.h"

char	*ft_strchr(const char *s, int c)
{
	const char *buf;

	buf = s;
	while (buf && *buf && *s)
	{
		if (*buf == (unsigned char)c)
			return ((char *)(buf));
		buf++;
	}
	if (c == 0)
		return ((char *)(buf));
	return (NULL);
}
