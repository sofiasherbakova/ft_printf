#include "ft_printf.h"

char	*ft_strnew(size_t size)
{
	char	*new;
	size_t	i;

	i = 0;
	new = (char *)malloc(size + 1);
	if (new == NULL)
		return (NULL);
	while (i < size + 1)
		new[i++] = '\0';
	return (new);
}
